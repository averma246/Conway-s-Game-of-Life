/**
 * A <code>Cell</code> that is always dead.
 * @author Ana Verma averma20@amherst.edu
 **/
// =============================================================================


// =============================================================================
public class ZombieCell extends Cell{
// =============================================================================
    /**
     * Constructor which creates the always dead cell.Uses empty constructor of
     * super class <code>Cell</code>.
     **/
    public ZombieCell(){
	super();
	_isAlive = false;
    }
    // =========================================================================

    // =========================================================================

    /**
     * Visual representation of the cell: empty. It is a cell that is off the
     * grid, so it is not shown.
     **/
    public String toString(){
	return"";
    }
    // =========================================================================

    // =========================================================================
    /**
     * This type of cell has no evolve method--it is not a part of the grid.
     **/
    public void evolve(){}

    // =========================================================================

    // =========================================================================

// =============================================================================
}// class ZombieCell
// =============================================================================
